public class algoLanjut {
    public static void main(String[] args){
        for(int i=7; i < 100; i += 7){
            System.out.println(i + " ");
        }
        int x = 7;
        while(x < 7){
            System.out.println(x);
            x += 7;
        }
        do{
            System.out.println(x);
            x += 7;
        }while(x < 100);
    }
}



















